COLORS = [[0.000, 0.447, 0.741], [0.850, 0.325, 0.098], [0.929, 0.694, 0.125],
          [0.494, 0.184, 0.556], [0.466, 0.674, 0.188], [0.301, 0.745, 0.933]]

def plot_results(pil_img, prob, boxes):
    plt.figure(figsize=(16,10))
    plt.imshow(pil_img)
    ax = plt.gca()
    colors = COLORS * 100
    for p, (xmin, ymin, xmax, ymax), c in zip(prob, boxes.tolist(), colors):
        ax.add_patch(plt.Rectangle((xmin, ymin), xmax - xmin, ymax - ymin,
                                   fill=False, color=c, linewidth=3))
        cl = p.argmax()
        print(f"p: {p}")
        print(f"cl: {cl}")
        print(f"CLASSES[cl]: {CLASSES[cl]}")
        print(f"p[cl]: {p[cl]}")
        text = f'{CLASSES[cl]}: {p[cl]:0.2f}'
        ax.text(xmin, ymin, text, fontsize=15,
                bbox=dict(facecolor='yellow', alpha=0.5))
    plt.axis('off')
    plt.savefig("test.png")

url = "http://images.cocodataset.org/val2017/000000039769.jpg"
image = Image.open(requests.get(url, stream=True).raw)

data_dir = "./data"
img_files = os.listdir(data_dir)[1:]

images = []
for i, img_file in enumerate(img_files):
    img_path = os.path.join(data_dir, img_file)
    img = Image.open(img_path)
    img = img.resize((256,256))
    print(img.size)
    images.append(img)

print(f"Length of images: {len(images)}")

target_sizes = torch.tensor(np.ones((len(images), 2)) * 256)
# print("test", target_sizes)

# you can specify the revision tag if you don't want the timm dependency
processor = DetrImageProcessor.from_pretrained("facebook/detr-resnet-50", revision="no_timm")
model = DetrForObjectDetection.from_pretrained("facebook/detr-resnet-50", revision="no_timm")

inputs = processor(images=image, return_tensors="pt")
outputs = model(**inputs)

# convert outputs (bounding boxes and class logits) to COCO API
# let's only keep detections with score > 0.9
target_sizes = torch.tensor([image.size[::-1]])
# print(f"target size: {target_sizes}, {target_sizes.shape}")
all_results = processor.post_process_object_detection(outputs, target_sizes=target_sizes, threshold=0.5)
results = all_results[0]

print(f"All Results: {all_results}")

for score, label, box in zip(results["scores"], results["labels"], results["boxes"]):
    box = [round(i, 2) for i in box.tolist()]
    print(
            f"Detected {model.config.id2label[label.item()]} with confidence "
            f"{round(score.item(), 3)} at location {box}"
    )

plot_results(images[0], results["scores"], results["boxes"])